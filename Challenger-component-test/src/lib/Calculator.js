class Calculator {
  sum(number1, number2){
    return number1 + number2;
  }

  subtraction(number1, number2){
    return number1 - number2;
  }

  multiplication(number1, number2) {
    return number1 * number2;
  }

  division(number1, number2) {
    return number1 / number2;
  }
}

module.exports = Calculator;